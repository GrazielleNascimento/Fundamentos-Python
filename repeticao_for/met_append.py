lista = []        # Começa com uma lista vazia
lista.append(1)   # Adiciona o número 1 no final da lista -> lista agora é [1]
lista.append(2)   # Adiciona o número 2 no final da lista -> lista agora é [1, 2]
lista.append(3)   # Adiciona o número 3 no final da lista -> lista agora é [1, 2, 3]

print(lista)      # Saída: [1, 2, 3]

#Cada vez que append é chamado, o estado da lista muda, 
# adicionando um novo elemento ao seu final.
